const kMostFreqWords = (words, k) => {
  let wordMap = {};
  let result = [];

  for (word of words) {
    if (word in wordMap) {
      wordMap[word]++;
    } else {
      wordMap[word] = 1;
    }
  }
  console.log(wordMap);
  let sortedWords = Object.keys(wordMap).sort((a, b) => {
    if (wordMap[b] === wordMap[a]) {
      if (b > a) return -1;
      else return 1;
    }
    return wordMap[b] - wordMap[a];
  });

  for (let i = 0; i < k; i++) {
    result.push(sortedWords[i]);
  }
  return result;
};

